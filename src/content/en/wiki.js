  "> See the full timeline of how this led to the creation of Bitcoin in [Blockchain Technology](blockchain).",
  "",
  "---",
  "",
  "## How cryptography protects the blockchain",
  "",
  "**Authenticity:** Each transaction is signed with the sender's private key. Only the key holder can authorize a transfer — but anyone can verify the signature is legitimate using the public key.",
  "",
  "**Integrity:** The hash of each block includes the hash of the previous block. Altering any old block invalidates all subsequent blocks — making fraud computationally unfeasible.",
  "",
  "**Decentralization:** Because verification is mathematical, there's no need for a central authority. Any node can confirm a transaction is valid using only cryptographic rules.",
