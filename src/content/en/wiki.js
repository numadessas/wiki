  "",
  "## Proof of Work (PoW)",
  "",
  "**Mining:** Miners compete to solve a complex mathematical problem (cryptographic hash). The first miner to find the solution *proves* their work, adds the block to the blockchain, and is rewarded with cryptocurrency.",
  "",
  "**Validation:** If the majority of other nodes agree with the miner's work, the block is added to the blockchain.",
