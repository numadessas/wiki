  "",
  "---",
  "",
  "## Proof of Stake (PoS)",
  "",
  "**Mining:** No mining. Validators are chosen to propose and validate blocks based on the amount of cryptocurrency they hold and are willing to *stake*. The larger the stake, the higher the chance of being selected.",
  "",
  "**Validation:** Other validators verify the validity of the block. If the majority agrees, the block is added to the blockchain.",
  "",
  "**Examples:** Ethereum (ETH) · Cardano (ADA) · Tezos (XTZ) · Avalanche (AVAX) · Algorand (ALGO)",
  "",
  "---",
  "",
  "## Proof of Authority (PoA)",
  "",
