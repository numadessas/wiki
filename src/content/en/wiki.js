  "## The Merkle Tree",
  "",
  "One of the most important structures for the blockchain is the **Merkle Tree**, created by Ralph Merkle in 1979.",
  "",
  "Here's how it works: each transaction generates a unique code called a *hash*. These hashes are grouped in pairs and combined into new hashes — and so on, until a single hash remains at the top, called the **Merkle Root**.",
